#include <iostream>
using namespace std;

int main() {
    int num1 = 10;
    int num2 = 20;

    // Use the compound assignment operator to add 5 to num1
    num1 += 5;

    // Use the compound assignment operator to subtract 10 from num2
    num2 -= 10;

    // Output the final values of num1 and num2
    cout << "The final value of num1 is: " << num1 << endl;
    cout << "The final value of num2 is: " << num2 << endl;

    return 0;
}
